def nearest_neighbor_interpolation(img, scale_factor):
    h, w = img.shape[:2]
    new_h, new_w = int(h * scale_factor), int(w * scale_factor)

    if len(img.shape) == 3:
        zoomed = np.zeros((new_h, new_w, img.shape[2]), dtype=img.dtype)
        for c in range(img.shape[2]):
            for i in range(new_h):
                for j in range(new_w):
                    orig_i = min(int(i / scale_factor), h-1)
                    orig_j = min(int(j / scale_factor), w-1)
                    zoomed[i, j, c] = img[orig_i, orig_j, c]
    else:
        zoomed = np.zeros((new_h, new_w), dtype=img.dtype)
        for i in range(new_h):
            for j in range(new_w):
                orig_i = min(int(i / scale_factor), h-1)
                orig_j = min(int(j / scale_factor), w-1)
                zoomed[i, j] = img[orig_i, orig_j]

    return zoomed

def bilinear_interpolation(img, scale_factor):
    h, w = img.shape[:2]
    new_h, new_w = int(h * scale_factor), int(w * scale_factor)

    if len(img.shape) == 3:
        zoomed = np.zeros((new_h, new_w, img.shape[2]), dtype=np.float32)
        for c in range(img.shape[2]):
            for i in range(new_h):
                for j in range(new_w):
                    x = i / scale_factor
                    y = j / scale_factor

                    x1, y1 = int(np.floor(x)), int(np.floor(y))
                    x2, y2 = min(x1+1, h-1), min(y1+1, w-1)

                    wx = x - x1
                    wy = y - y1

                    zoomed[i, j, c] = (1-wx)*(1-wy)*img[x1, y1, c] + \
                                       wx*(1-wy)*img[x2, y1, c] + \
                                       (1-wx)*wy*img[x1, y2, c] + \
                                       wx*wy*img[x2, y2, c]
    else:
        zoomed = np.zeros((new_h, new_w), dtype=np.float32)
        for i in range(new_h):
            for j in range(new_w):
                x = i / scale_factor
                y = j / scale_factor

                x1, y1 = int(np.floor(x)), int(np.floor(y))
                x2, y2 = min(x1+1, h-1), min(y1+1, w-1)

                wx = x - x1
                wy = y - y1

                zoomed[i, j] = (1-wx)*(1-wy)*img[x1, y1] + \
                               wx*(1-wy)*img[x2, y1] + \
                               (1-wx)*wy*img[x1, y2] + \
                               wx*wy*img[x2, y2]

    return zoomed.astype(img.dtype)

def compute_nssd(img1, img2):
    # Ensure both images have the same dimensions
    if img1.shape != img2.shape:
        img2_resized = cv2.resize(img2, (img1.shape[1], img1.shape[0]))
    else:
        img2_resized = img2

    diff = img1.astype(np.float32) - img2_resized.astype(np.float32)
    ssd = np.sum(diff ** 2)
    return ssd / img1.size

