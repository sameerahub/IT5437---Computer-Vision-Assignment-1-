def create_gaussian_kernel(size, sigma):
    kernel = np.zeros((size, size))
    center = size // 2

    for i in range(size):
        for j in range(size):
            x = i - center
            y = j - center
            kernel[i, j] = (1 / (2 * np.pi * sigma**2)) * \
                          np.exp(-(x**2 + y**2) / (2 * sigma**2))

    return kernel / kernel.sum()

