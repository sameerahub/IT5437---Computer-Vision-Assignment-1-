def manual_bilateral_filter(img, diameter, sigma_s, sigma_r):
    if len(img.shape) == 3:
        gray = cv2.cvtColor(img, cv2.COLOR_BGR2GRAY)
    else:
        gray = img.astype(np.float32)

    h, w = gray.shape
    result = np.zeros_like(gray, dtype=np.float32)
    radius = diameter // 2

    spatial_kernel = np.zeros((diameter, diameter), dtype=np.float32)
    for i in range(diameter):
        for j in range(diameter):
            dx = i - radius
            dy = j - radius
            spatial_kernel[i, j] = np.exp(-(dx**2 + dy**2) / (2 * sigma_s**2))

    padded = np.pad(gray, radius, mode='reflect')

    for i in range(h):
        for j in range(w):
            window = padded[i:i+diameter, j:j+diameter]
            center_val = gray[i, j]

            range_kernel = np.exp(-((window - center_val)**2) / (2 * sigma_r**2))
            combined = spatial_kernel * range_kernel
            combined = combined / (combined.sum() + 1e-10)

            result[i, j] = np.sum(window * combined)

    return result.astype(np.uint8)

