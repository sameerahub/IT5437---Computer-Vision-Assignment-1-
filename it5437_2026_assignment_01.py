def create_dog_kernel(size, sigma, direction='x'):
    kernel = np.zeros((size, size))
    center = size // 2

    for i in range(size):
        for j in range(size):
            x = i - center
            y = j - center
            g = (1 / (2 * np.pi * sigma**2)) * np.exp(-(x**2 + y**2) / (2 * sigma**2))

            if direction == 'x':
                kernel[i, j] = -x / (sigma**2) * g
            else:
                kernel[i, j] = -y / (sigma**2) * g

    return kernel

