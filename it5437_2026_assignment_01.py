def homomorphic_filter(img, cutoff=30, gamma_l=0.5, gamma_h=1.5, c=1):
    if len(img.shape) == 3:
        gray = cv2.cvtColor(img, cv2.COLOR_BGR2GRAY)
    else:
        gray = img.copy()

    gray_float = gray.astype(np.float32)
    img_log = np.log1p(gray_float)

    f = np.fft.fft2(img_log)
    fshift = np.fft.fftshift(f)

    rows, cols = gray.shape
    crow, ccol = rows // 2, cols // 2

    u = np.arange(rows) - crow
    v = np.arange(cols) - ccol
    U, V = np.meshgrid(u, v, indexing='ij')
    D = np.sqrt(U**2 + V**2)

    H = (gamma_h - gamma_l) * (1 - np.exp(-c * (D**2) / (cutoff**2))) + gamma_l

    fshift_filtered = fshift * H
    f_ishift = np.fft.ifftshift(fshift_filtered)
    img_back = np.fft.ifft2(f_ishift)
    img_back = np.real(img_back)

    img_filtered = np.expm1(img_back)
    img_filtered = cv2.normalize(img_filtered, None, 0, 255, cv2.NORM_MINMAX)

    return img_filtered.astype(np.uint8), H

